_BACKEND_MAIN_WITH_DB = """\
from contextlib import asynccontextmanager

import asyncpg
from fastapi import FastAPI

from app.db import init_pool, close_pool, get_pool


@asynccontextmanager
async def _lifespan(app: FastAPI):
    await init_pool()
    yield
    await close_pool()


app = FastAPI(title="{project_name}", lifespan=_lifespan)


@app.get("/health")
async def health() -> dict[str, str]:
    pool: asyncpg.Pool = get_pool()
    async with pool.acquire() as conn:
        val = await conn.fetchval("SELECT 1")
    return {{"status": "ok", "db": int(val) if val else 0}}
"""

_BACKEND_DB_PY = """\
\"\"\"asyncpg pool lifecycle. Imported by app.main's lifespan.\"\"\"

from __future__ import annotations

import os

import asyncpg

_pool: asyncpg.Pool | None = None


def _dsn() -> str:
    return os.environ.get(
        "DATABASE_URL",
        "postgres://postgres:postgres@postgres:5432/{db_name}",
    )


async def init_pool() -> None:
    global _pool
    _pool = await asyncpg.create_pool(_dsn(), min_size=1, max_size=5)


async def close_pool() -> None:
    global _pool
    if _pool is not None:
        await _pool.close()
        _pool = None


def get_pool() -> asyncpg.Pool:
    if _pool is None:
        raise RuntimeError("DB pool not initialised; lifespan didn't run.")
    return _pool
"""

