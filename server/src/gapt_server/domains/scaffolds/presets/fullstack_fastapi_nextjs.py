def _render(ctx: RenderContext) -> dict[str, bytes]:
    primary_port = int(ctx.options.get("primary_port", 80))
    database = str(ctx.options.get("database", "none"))
    db_name = "app"  # baked default; backend preset exposes db_name as an option.

    files: dict[str, bytes] = {
        "README.md": _README.format(
            project_name=ctx.project_name,
            github_owner=ctx.github_owner,
            repo_name=ctx.repo_name,
            primary_port=primary_port,
            database_section=_DATABASE_POSTGRES if database == "postgres" else _DATABASE_NONE,
        ).encode("utf-8"),
        ".gitignore": combined_gitignore(PYTHON_GITIGNORE, NODE_GITIGNORE),
        "LICENSE": mit_license(ctx.github_owner),
        # ── backend
        "backend/app/__init__.py": b"",
        "backend/Dockerfile": _BACKEND_DOCKERFILE.encode("utf-8"),
        # ── frontend
        "frontend/app/page.tsx": _FRONTEND_PAGE.format(project_name=ctx.project_name).encode("utf-8"),
        "frontend/app/layout.tsx": _FRONTEND_LAYOUT.format(project_name=ctx.project_name).encode("utf-8"),
        "frontend/package.json": _FRONTEND_PACKAGE_JSON.format(slug=ctx.slug).encode("utf-8"),
        "frontend/next.config.mjs": _FRONTEND_NEXT_CONFIG.encode("utf-8"),
        "frontend/tsconfig.json": _FRONTEND_TSCONFIG.encode("utf-8"),
        "frontend/Dockerfile": _FRONTEND_DOCKERFILE.encode("utf-8"),
        # ── nginx
        "nginx/nginx.conf": _NGINX_CONF.encode("utf-8"),
        "nginx/Dockerfile": _NGINX_DOCKERFILE.encode("utf-8"),
    }
    if database == "postgres":
        files["backend/app/main.py"] = _BACKEND_MAIN_WITH_DB.format(
            project_name=ctx.project_name
        ).encode("utf-8")
        files["backend/app/db.py"] = _BACKEND_DB_PY.format(db_name=db_name).encode("utf-8")
        files["backend/requirements.txt"] = _BACKEND_REQS_WITH_DB.encode("utf-8")
        files["backend/alembic.ini"] = _ALEMBIC_INI.format(db_name=db_name).encode("utf-8")
        files["backend/alembic/env.py"] = _ALEMBIC_ENV.encode("utf-8")
        files["backend/alembic/versions/.gitkeep"] = b""
        files["docker-compose.yml"] = _COMPOSE_WITH_DB.format(
            primary_port=primary_port, db_name=db_name
        ).encode("utf-8")
    else:
        files["backend/app/main.py"] = _BACKEND_MAIN_NO_DB.format(
            project_name=ctx.project_name
        ).encode("utf-8")
        files["backend/requirements.txt"] = _BACKEND_REQS_NO_DB.encode("utf-8")
        files["docker-compose.yml"] = _COMPOSE_NO_DB.format(
            primary_port=primary_port
        ).encode("utf-8")
    return files


